// ===== 写入数据 =====
const insert = db.prepare(`
  INSERT INTO templates (weekday, time, courseName, teacher, danceType, level, createdAt, updatedAt)
  VALUES (?, ?, ?, ?, ?, ?, ?, ?)
`);

const now = Date.now();

const insertMany = db.transaction((courses) => {
  let count = 0;
  for (const [weekday, time, courseName, danceType, level, teacher] of courses) {
    insert.run(weekday, time, courseName, teacher, danceType, level, now, now);
    count++;
  }
  return count;
});

const count = insertMany(courses);
